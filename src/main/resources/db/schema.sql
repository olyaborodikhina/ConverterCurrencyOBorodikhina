CREATE TABLE USER(
  ID         INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) PRIMARY KEY,
  LOGIN      varchar(15) not null ,
  PASSWORD   varchar(15) not null
);

CREATE TABLE ACCOUNT(
  ID    INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) PRIMARY KEY,
  ACCOUNT_EUR INT,
  ACCOUNT_USD INT,
  ACCOUNT_RUB INT
);

